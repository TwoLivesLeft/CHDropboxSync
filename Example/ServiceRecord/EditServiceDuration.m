- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Configure the cell...
    cell.detailTextLabel.text = nil;
    BOOL selected=NO;
    if (indexPath.row==0) {
        cell.textLabel.text = @"By odometer"; 
        cell.detailTextLabel.text = @"You won't get service reminders";
        selected = self.vehicle.serviceIntervalMonths<=0;
    } else {
        int months = indexPath.row*6;
        cell.textLabel.text = $str(@"Every %d months", months);
        selected = self.vehicle.serviceIntervalMonths==months;
    }
    cell.accessoryType = selected ? UITableViewCellAccessoryCheckmark : 0;
    
    return cell;
}

